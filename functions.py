# Functions - Python's Superpowers!
# Functions are blocks of reusable code -- they save you from repeating the same,
# code over and over. Think of functions like superpowers you can call whenever you
# need them!

# Example:

# Defining a function
def greet(name):
    print("Hello, " + name + "! ")

# Calling the function
greet("Alice")
greet("Bob")

# Return values
# Functions can also return values!

def add_numbers(x, y):
    return x + y

result = add_numbers(10, 5)
print(result)

# Functions take input (called parameters) and give you back output. You can
# reuse them as many times as you want!

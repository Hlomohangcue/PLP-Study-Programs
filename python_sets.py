# Define two sets
A = {1,2,3,4}
B = {3,4,5,6}

# Set operations in action
union_set = A.union(B)

intersection_set = A.intersection(B)

difference_set = A.difference(B)

print("set A: ", A)
print("set B: ", B)
print("Union: ", union_set)
print("intersection: ", intersection_set)
print("Difference (A - B): ", difference_set)
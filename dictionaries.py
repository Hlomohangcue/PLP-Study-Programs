# Dictionaries
# Store key-value pairs. Think of it as a real-life dictionary, where you look up a word(key) to get its defination(value).
# A dictionary is like a phonebook--It stores key-value pairs.
# Example

student_info = {"name": "Charlie", "age": 25, "grade": "A"}

# Accessing a value by its key
print(student_info["name"])

# Modifying a value
student_info["age"] = 26
print(student_info)
# Lists
# Python gives us super cool ways to organize lots of data!
# Lists : ordered collections that are mutable (changeable). Great for storing multiple items.
# A List is like a collection of items (e.g.,a shopping list).

fruits = ["Apple", "Banana", "cherry"]

# Accessing elements
print(fruits[0])

# Modifying an element
fruits[1] = "Orange"
print(fruits)

# Adding an element
fruits.append("Grapes")
print(fruits)
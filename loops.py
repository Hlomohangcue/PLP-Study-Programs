# Loops- Repeating tasks like a Pro!
# Loops help us repeat tasks without writing the same code over and over. Think of it like pressing the repeat button on your favorite song.
# For Loop: Used for repeating tasks over a range.
# Repeats a block of code a fixed number of times.

# Print numbers 0 to 5
for i in range(5):
    print(i)

# While Loop: Runs as long as a condition is True.
# Repeats as long as a condition is true.
# Example:
countdown = 5

while countdown > 0:
    print("Counting down: ", countdown)
    countdown -= 1

print("Blast off!")